export const allLanguages: LanguageConfig[] = [
  ...mainstreamLanguages,
  ...webLanguages,
  ...nicheLanguages,
];

export const languageExtensionMap: Record<string, string> = {};
export const languageFilenameMap: Record<string, string> = {};
export const languageAliasMap: Record<string, string> = {};
export const monacoLanguageIdMap: Record<string, string> = {};

allLanguages.forEach((lang) => {
  lang.extensions.forEach((ext) => {
    const normalizedExt = ext.startsWith('.') ? ext.toLowerCase() : `.${ext}`.toLowerCase();
    languageExtensionMap[normalizedExt] = lang.id;
  });

  lang.filenames?.forEach((filename) => {
    languageFilenameMap[filename.toLowerCase()] = lang.id;
  });

  lang.aliases?.forEach((alias) => {
    languageAliasMap[alias.toLowerCase()] = lang.id;
  });

  monacoLanguageIdMap[lang.id] = lang.monacoLanguageId;
});

export function getLanguageFromExtension(extension: string): string {
  const normalizedExt = extension.startsWith('.')
    ? extension.toLowerCase()
    : `.${extension}`.toLowerCase();
  return languageExtensionMap[normalizedExt] || 'plaintext';
}

export function getLanguageFromFilename(filename: string): string {
  const lowerFilename = filename.toLowerCase();
  if (languageFilenameMap[lowerFilename]) {
    return languageFilenameMap[lowerFilename];
  }
  const ext = filename.split('.').pop()?.toLowerCase() || '';
  return getLanguageFromExtension(ext);
}

export function getLanguageConfig(languageId: string): LanguageConfig | undefined {
  return allLanguages.find((lang) => lang.id === languageId);
}

export function getMonacoLanguageId(languageId: string): string {
  return monacoLanguageIdMap[languageId] || 'plaintext';
}

export function getLanguageIcon(filename: string, isDirectory: boolean): string {
  if (isDirectory) return 'üìÅ';
  const langId = getLanguageFromFilename(filename);
  const config = getLanguageConfig(langId);
  return config?.icon || 'üìÑ';
}

